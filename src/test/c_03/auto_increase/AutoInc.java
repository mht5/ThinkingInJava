package test.c_03.auto_increase;

public class AutoInc {
	public static void main(String[] args){
		int i=6;
		System.out.println("i="+i);
		System.out.println("++i="+ ++i);
		System.out.println("i++="+i++);
		System.out.println("i="+i);
		System.out.println("--i="+ --i);
		System.out.println("i--="+i--);
		System.out.println("i="+i);
	}
}
