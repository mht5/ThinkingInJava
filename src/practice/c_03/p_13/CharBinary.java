package practice.c_03.p_13;

public class CharBinary {
	public static void main(String[] args){
		char c='a';
		System.out.println(Integer.toBinaryString(c));
		c='m';
		System.out.println(Integer.toBinaryString(c));
		c='c';
		System.out.println(Integer.toBinaryString(c));
		c='H';
		System.out.println(Integer.toBinaryString(c));
		c='B';
		System.out.println(Integer.toBinaryString(c));
		c='O';
		System.out.println(Integer.toBinaryString(c));
	}
}
